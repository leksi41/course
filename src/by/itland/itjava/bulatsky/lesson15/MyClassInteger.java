package by.itland.itjava.bulatsky.lesson15;

public class MyClassInteger {

    int a;
    int b;


    public MyClassInteger(int a, int b) {
        this.a = a;
        this.b = b;
    }
    @Override
    public String toString() {
        return "MyClassInteger{" +
                "a=" + a +
                ", b=" + b +
                '}';
    }
}
