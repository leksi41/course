package by.itland.itjava.bulatsky.lesson15;

public class MyClassDouble {

    double a;
    double b;

    public MyClassDouble(double a, double b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "MyClassDouble{" +
                "a=" + a +
                ", b=" + b +
                '}';
    }
}
